int main(void)
{

    /* --- INICIALIZACIONES. SISTEMA ------------------------------------- */

	SYSTEM_Ini(0);

	/* --- BUCLE PRINCIPAL ----------------------------------------------- */

	for(;;)
	{

		uint32_t stat1 = SWITCH_GetSW1() & SWITCH_BIT_ACTUALSTAT;
		uint32_t stat2 = SWITCH_GetSW2() & SWITCH_BIT_ACTUALSTAT;

		if( (stat1 != 0) && (stat2 != 0) )
		{
		    LED_Set(0xFFFFFFFF);
		}
		else if (stat1 != 0)
		{
			LED_Set(0x55555555);
		}
		else if (stat2 != 0)
		{
			LED_Set(0xFF00FF00);
		}
		else
		{
			LED_Set(0x00000000);
		}
	}

	/* --- NO DEBE LLEGAR AQUÍ PERO... ----------------------------------- */

	//SYSTEM_ReInit();

}


